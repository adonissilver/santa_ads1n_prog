# Exercício 4 – Comissão de vendedor
# Peça o valor de uma venda e calcule a comissão de 5%
#
 
venda=float(input("Digite valor da venda: "))

porcentagem=float(input("Insira a porcentagem (sem o símbolo de porcentagem):  "))


print("Valor da comissao com porcentagem: ",(venda*(1-(porcentagem/100) ))  )







